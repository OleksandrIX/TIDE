def get_allowed_languages_for_sphinx() -> list[str]:
    """
    Returns allowed model languages for Sphinx to convert
    All language models stores in PATH_TO_MODELS
    """

    return [file for file in os.listdir(get_model_path()) if not file.endswith('.py')]


def get_allowed_languages_for_whisper() -> list[str]:
    return [LANGUAGES[key] for key in LANGUAGES]


def get_language_name_from_bcp_47(bcp47_lang: str) -> str | None:
    return LANGUAGES.get(bcp47_lang.split("-")[0], None)


def get_model_path():
    import speech_recognition
    from os import path

    return path.join(path.dirname(speech_recognition.__file__), "pocketsphinx-data")
